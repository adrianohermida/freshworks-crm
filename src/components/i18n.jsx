export function useI18n(language = 'pt') {
  const t = (key) => {
    const keys = key.split('.');
    let value = translations[language] || translations.pt;
    
    for (const k of keys) {
      value = value[k];
      if (!value) return key;
    }
    
    return value;
  };

  return { t, language, languages: ['pt', 'en', 'es'] };
}

export function useLanguage() {
  const stored = typeof window !== 'undefined' ? localStorage.getItem('language') || 'pt' : 'pt';
  
  const setLanguage = (lang) => {
    if (typeof window !== 'undefined') {
      localStorage.setItem('language', lang);
      window.location.reload();
    }
  };

  return { language: stored, setLanguage };
}

